package com.in.test;

public class SwitchExample1 {

	public static void main(String[] args) {
		
	int choice=6;
	switch(choice)
	{
		case 001 ->  System.out.println("Hi");
		case 002 ->  System.out.println("Hello");
		case 003 ->  System.out.println("welcome");
		default -> System.out.println("wrong choice!!");
	}

}
}
